package Coding;

public class PrintEvenOddArray {
public static void main(String[] args) {
	
	
	
	int arr[]= {1,2,3,4,5};
	
	
	for(int i=0; i<=arr.length-1; i++) {
		
		if(arr[i]%2==0) {
			System.out.println(arr[i] + "EVEN NUMBER"  );
		}else {
			System.out.println(arr[i]+"it is odd");
		}
		
	}
	
	
}
	
	
}
